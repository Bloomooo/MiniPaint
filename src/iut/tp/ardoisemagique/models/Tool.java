package iut.tp.ardoisemagique.models;

/**
 * L'énumération Tool représente les différents outils de dessin disponibles.
 * 
 * @author Yanis Mechta
 * @version 1.0
 */
public enum Tool {
    /**
     * Outil de pipette. Permet de choisir une couleur en cliquant sur la toile.
     */
    CHOOSER,

    /**
     * Outil de dessin rond. Dessine des formes rondes.
     */
    ROUND,

    /**
     * Outil de dessin carré. Dessine des formes carrées.
     */
    SQUARE
}
